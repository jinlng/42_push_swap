#include "libft.h"

long	ft_atol(const char *s)
{
	long	n;
	int		sign;

	n = 0;
	sign = 1;
	if (*s == '+' || *s == '-')
	{
		if (*s++ == '-')
			sign = -1;
	}
	while (ft_isdigit(*s))
		n = n * 10 + (*s++ - '0');
	return (n * sign);
}
