#include "libft.h"

char	*ft_strtrim(const char *s1, const char *set)
{
	size_t	size;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	size = ft_strlen(s1);
	while (size && ft_strchr(set, s1[size - 1]))
		size--;
	return (ft_substr(s1, 0, size));
}
